// This function adds two numbers and returns the result.
int add(int a, int b) {
  return (a+b);
}
// This function subtracts two numbers and returns the result.
int sub(int a, int b) {
  return (a-b);
}
// This function multiplys two numbers and returns the result.
int mult(int a, int b) {
  return (a*b);
}
// This function divides two numbers and returns the result.
int divide(int a, int b) {
  return (a/b);
}
// This function divides two numbers and returns the remainder.
int mod(int a, int b) {
  return (a%b);
}
